        cmbestado.addItem("Finalizado");    
        
        // Cargar los datos desde el archivo de texto
        try (BufferedReader reader = new BufferedReader(new FileReader("datos.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] info = line.split(",");
                modelo.addRow(info);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

