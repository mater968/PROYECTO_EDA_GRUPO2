    private void btnmovertramiteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmovertramiteActionPerformed
        // TODO add your handling code here
        String [] info = new String[4];
        info[0]= txtdni.getText(); 
        info[1]= txtdi.getText();    
        if (cmbdf.getSelectedItem().toString().equals("-Selecciona-")){ JOptionPane.showMessageDialog(null, "Debes de elegir una opción en Destino Final");}
        else{ txtdni.setText("");
              txtdi.setText("");
              info[2]= cmbdf.getSelectedItem().toString();  }   
        if (cmbestado.getSelectedItem().toString().equals("-Selecciona-")){ JOptionPane.showMessageDialog(null, "Debes de elegir una opción en Estado del Tramite");}
        else{ txtdni.setText("");
              txtdi.setText("");
              info[3]= cmbestado.getSelectedItem().toString(); modelo.addRow(info);  }
        // Guardar los datos en un archivo de texto
        try (PrintWriter writer = new PrintWriter(new FileWriter("datos.txt", true))) {
            for (int i = 0; i < modelo.getRowCount(); i++) {
            writer.println(modelo.getValueAt(i, 0) + "," + modelo.getValueAt(i, 1) + "," + modelo.getValueAt(i, 2) + "," + modelo.getValueAt(i, 3));
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnmovertramiteActionPerformed

